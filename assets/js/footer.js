// Display footer menu for responsive size 
const optionMenus = document.querySelectorAll(".footer--item");
optionMenus.forEach((optionMenu) => {
  const selectBtn = optionMenu.querySelector(".footer--item-btn");
  const options = optionMenu.querySelectorAll(".footer--sub-item");
  const sBtn_text = optionMenu.querySelector(".link-text");

  selectBtn.addEventListener("click", () => {
    optionMenu.classList.toggle("footer-link-active");
  });

  options.forEach((option) => {
    option.addEventListener("click", () => {
      let selectedOption = option.textContent;
      sBtn_text.textContent = selectedOption;
      optionMenu.classList.remove("footer-link-active");
    });
  });
});
